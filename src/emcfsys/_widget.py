    def auto_parse_label_mapping(self, text: str):
        """
        自动识别 JSON / Python dict / YAML / key-value / CSV 等格式，返回 dict。
        """

        text = text.strip()

        # 1. 尝试 JSON（最规范）
        try:
            return json.loads(text)
        except Exception:
            pass

        # 2. 尝试 Python 字典格式
        try:
            return ast.literal_eval(text)
        except Exception:
            pass

        # 3. 尝试 YAML 风格：key: value
        try:
            lines = text.splitlines()
            mapping = {}
            for line in lines:
                if ":" not in line:
                    continue
                key, value = line.split(":", 1)
                key = key.strip()
                value = value.strip()
                if value.isdigit():
                    value = int(value)
                mapping[key] = value
            if mapping:
                return mapping
        except Exception:
            pass

        # 4. 尝试 CSV 或 key,value
        try:
            lines = text.splitlines()
            mapping = {}
            for line in lines:
                if "," not in line:
                    continue
                key, value = line.split(",", 1)
                key = key.strip()
                value = value.strip()
                if value.isdigit():
                    value = int(value)
                mapping[key] = value
            if mapping:
                return mapping
        except Exception:
            pass

        raise ValueError("无法解析标签映射文件，请检查文件格式。")

